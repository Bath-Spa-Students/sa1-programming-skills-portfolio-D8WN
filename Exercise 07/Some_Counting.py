# Exercise 07: Some Counting

# 0 to 50 in increments of 1
for d in range(50):
    print(d)

# 50 to 0 in decrements of 1
for a in range(50, 0):
    print(a)

# 30 to 50 in increments of 1
for w in range(30, 51):
    print(w)

# 50 to 10 in decrements of 2
for n in range(50, 9, 2):
    print(n)

# 100 to 200 in increments of 5
for i in range(100, 201, 5):
    print(i)